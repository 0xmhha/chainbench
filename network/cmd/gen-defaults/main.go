// Command gen-defaults renders the cross-layer default constants (SSOT-X1)
// from network/schema/defaults.json into one constant file per layer:
//
//	network/internal/types/defaults_gen.go   (Go)
//	lib/defaults.generated.sh                (bash)
//	mcp-server/src/utils/defaults.generated.ts (TypeScript)
//
// Run via `go generate ./...` from the network/ module root. Use -check to
// verify the committed files are up to date (non-zero exit on drift) without
// writing — used by the gen-defaults test and suitable for CI.
package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"os"
	"path/filepath"
)

// defaults mirrors network/schema/defaults.json. Adding a field here +
// rendering it below + adding it to defaults.json is the only edit needed to
// promote a new cross-layer default.
type defaults struct {
	Stablenet struct {
		ChainID int64 `json:"chain_id"`
	} `json:"stablenet"`
	Ports struct {
		BaseP2P     int `json:"base_p2p"`
		BaseHTTP    int `json:"base_http"`
		BaseWS      int `json:"base_ws"`
		BaseAuth    int `json:"base_auth"`
		BaseMetrics int `json:"base_metrics"`
	} `json:"ports"`
}

func main() {
	root := flag.String("root", ".", "repo root directory")
	check := flag.Bool("check", false, "verify committed files are up to date instead of writing")
	flag.Parse()

	srcPath := filepath.Join(*root, "network", "schema", "defaults.json")
	raw, err := os.ReadFile(srcPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "gen-defaults: read %s: %v\n", srcPath, err)
		os.Exit(1)
	}
	var d defaults
	if err := json.Unmarshal(raw, &d); err != nil {
		fmt.Fprintf(os.Stderr, "gen-defaults: parse %s: %v\n", srcPath, err)
		os.Exit(1)
	}

	files := render(d)
	if *check {
		drift := false
		for rel, want := range files {
			got, err := os.ReadFile(filepath.Join(*root, rel))
			if err != nil || string(got) != want {
				fmt.Fprintf(os.Stderr, "gen-defaults: %s is stale; run `go generate ./...` in network/\n", rel)
				drift = true
			}
		}
		if drift {
			os.Exit(1)
		}
		return
	}

	for rel, content := range files {
		path := filepath.Join(*root, rel)
		if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
			fmt.Fprintf(os.Stderr, "gen-defaults: write %s: %v\n", path, err)
			os.Exit(1)
		}
	}
}

// render returns the full content of each generated file keyed by its path
// relative to the repo root. Pure function of d so the gen-defaults test can
// diff it against the committed files without spawning a subprocess.
func render(d defaults) map[string]string {
	goFile := fmt.Sprintf(`// Code generated by gen-defaults; DO NOT EDIT.
// Source: network/schema/defaults.json

package types

// StablenetChainID is the stablenet adapter's default EIP-155 chain ID,
// used as the fallback when a profile omits chain.chain_id and as the probe's
// known-chain-ID discriminator for stablenet.
const StablenetChainID int64 = %d

// Default base ports for node allocation (base + node_index). Mirror the
// bash profile defaults so every layer agrees on the same fallbacks.
const (
	DefaultBaseP2PPort     = %d
	DefaultBaseHTTPPort    = %d
	DefaultBaseWSPort      = %d
	DefaultBaseAuthPort    = %d
	DefaultBaseMetricsPort = %d
)
`,
		d.Stablenet.ChainID,
		d.Ports.BaseP2P, d.Ports.BaseHTTP, d.Ports.BaseWS, d.Ports.BaseAuth, d.Ports.BaseMetrics,
	)

	shFile := fmt.Sprintf(`# Code generated by gen-defaults; DO NOT EDIT.
# Source: network/schema/defaults.json
# shellcheck shell=bash

CB_STABLENET_CHAIN_ID=%d
CB_DEFAULT_BASE_P2P=%d
CB_DEFAULT_BASE_HTTP=%d
CB_DEFAULT_BASE_WS=%d
CB_DEFAULT_BASE_AUTH=%d
CB_DEFAULT_BASE_METRICS=%d
`,
		d.Stablenet.ChainID,
		d.Ports.BaseP2P, d.Ports.BaseHTTP, d.Ports.BaseWS, d.Ports.BaseAuth, d.Ports.BaseMetrics,
	)

	tsFile := fmt.Sprintf(`// Code generated by gen-defaults; DO NOT EDIT.
// Source: network/schema/defaults.json

export const STABLENET_CHAIN_ID = %d;
export const DEFAULT_BASE_P2P = %d;
export const DEFAULT_BASE_HTTP = %d;
export const DEFAULT_BASE_WS = %d;
export const DEFAULT_BASE_AUTH = %d;
export const DEFAULT_BASE_METRICS = %d;
`,
		d.Stablenet.ChainID,
		d.Ports.BaseP2P, d.Ports.BaseHTTP, d.Ports.BaseWS, d.Ports.BaseAuth, d.Ports.BaseMetrics,
	)

	return map[string]string{
		filepath.Join("network", "internal", "types", "defaults_gen.go"):     goFile,
		filepath.Join("lib", "defaults.generated.sh"):                        shFile,
		filepath.Join("mcp-server", "src", "utils", "defaults.generated.ts"): tsFile,
	}
}
