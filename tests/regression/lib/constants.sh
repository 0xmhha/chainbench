#!/usr/bin/env bash
# tests/regression/lib/constants.sh — regression 테스트 공유 상수
#
# 2개 이상의 테스트 파일에서 동일하게 쓰이는 상수를 중앙 관리. common.sh가 자동 로드.
# 결정#3: 로컬·폐쇄망이 대부분 동일한 체인 값을 쓰므로 여기(공용)에 둔다.
# 환경마다 갈리는 값(예: EPOCH_LENGTH)은 tests/env/*.env 가 담당한다.
# readonly를 쓰지 않는다 — 일부 테스트(c-07, h-*)가 시나리오에 맞춰 상수를 국소 재정의하므로.

[[ -n "${_CB_REGRESSION_CONSTANTS_LOADED:-}" ]] && return 0
_CB_REGRESSION_CONSTANTS_LOADED=1

# ============================================================================
# 체인 설정 (EPOCH_LENGTH는 환경별 — 프로파일 .env 참조)
# ============================================================================

CHAIN_ID=8283            # chainId (hex: 0x205B)

# ============================================================================
# 가스 정책 (Anzeon)
# ============================================================================

