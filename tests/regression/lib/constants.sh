NEW_GAS_TIP=30000000000000       # gasTip 거버넌스 제안 테스트용 변경값 (30,000 Gwei)

# ============================================================================
# 컨트랙트 주소
# ============================================================================

# secp256r1 (P-256) 프리컴파일 — RIP-7212, Boho 하드포크 이후 활성화
P256_PRECOMPILE="0x0000000000000000000000000000000000000100"

# ============================================================================
# Gas Limit
# ============================================================================

GAS_SIMPLE_TRANSFER=21000   # ETH 단순 전송 (EIP-2028 기본값)
GAS_GOV_LARGE=2000000       # 거버넌스 approve — addMember/removeMember 등 cold SSTORE 다수

# ============================================================================
# 금액 (Wei)
# ============================================================================

AMOUNT_0_001_ETHER=1000000000000000    # 0.001 WEMIX — 테스트 기본 송금액
AMOUNT_1_ETHER=1000000000000000000     # 1.0 WEMIX — 발행·소각·approve 기본 단위

# ============================================================================
# 타임아웃 (초)
# ============================================================================

TIMEOUT_TX_RECEIPT=30    # 일반 tx receipt 대기
TIMEOUT_TX_APPROVE=15    # 거버넌스 approve 단계 tx
TIMEOUT_EPOCH_WAIT=300   # 에폭 경계 블록 대기 (최대 2 에폭)

# ============================================================================
# 바이트코드
# ============================================================================

# BURN_BYTECODE: 배포 시 생성자에서 gasleft() > 100000 동안 tight loop 실행
